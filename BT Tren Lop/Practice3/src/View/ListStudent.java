    public void show(JTable a) {
        Vector<Student> list = StudentDB.getListStudent();
        DefaultTableModel model = (DefaultTableModel) a.getModel();
        model.setRowCount(0);
        Object[] row = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getScode();
            row[1] = list.get(i).getSname();
            row[2] = list.get(i).getDob();
            row[3] = list.get(i).getsGender();

            model.addRow(row);
        }
    }

